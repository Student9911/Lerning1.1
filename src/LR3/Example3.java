package LR3;



import java.util.Scanner;

public class Example3 {

    public static void main(String[] args) {
        Scanner in = new Scanner(System.in);
        int n;
        System.out.println("Введите количество итераций:");
        n = in.nextInt();
        int a = 1, b = 1;
        System.out.print(a + ", " + b);
        int c, i = 2;
        while (i < n) {
            c = a + b;
            a = b;
            b = c;
            System.out.print(", " + c);
            i++;
        }
    }
}
